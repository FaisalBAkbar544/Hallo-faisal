    <h1>Air Putih: Cairan Ajaib untuk Hidup Sehat</h1>

    <h2>Peran Air dalam Tubuh</h2>
    <p>Air merupakan komponen utama dalam tubuh manusia, mencakup sekitar 60% dari total berat badan. Air memiliki banyak fungsi vital, seperti mengatur suhu tubuh melalui keringat, membantu pelumasan sendi, melindungi organ dalam, serta mendukung proses metabolisme dan pencernaan. Selain itu, air juga berperan penting dalam mengangkut nutrisi dan oksigen ke seluruh sel tubuh serta membantu proses pengeluaran racun melalui urin dan keringat.</p>

    <p>Tanpa cukup air, tubuh akan kesulitan menjalankan fungsi-fungsi tersebut, yang dapat berujung pada dehidrasi. Gejala awal dehidrasi antara lain kelelahan, pusing, dan kulit kering. Jika dibiarkan, dehidrasi dapat menimbulkan komplikasi kesehatan yang lebih serius.</p>

    <h2>Dampak Minum Cukup Air</h2>
    <p>Memenuhi kebutuhan cairan setiap hari memberikan berbagai manfaat kesehatan, di antaranya:</p>
    <ul>
        <li><strong>Melancarkan pencernaan:</strong> Air membantu proses pencernaan makanan dan penyerapan nutrisi di usus.</li>
        <li><strong>Menjaga kesehatan kulit:</strong> Kulit yang cukup terhidrasi akan tampak lebih segar, lembut, dan bercahaya alami.</li>
        <li><strong>Menambah energi dan konsentrasi:</strong> Kekurangan air dapat menyebabkan rasa lelah dan gangguan fokus.</li>
        <li><strong>Menjaga fungsi ginjal:</strong> Air membantu ginjal menyaring limbah tubuh dan mencegah pembentukan batu ginjal.</li>
    </ul>

    <p>Konsumsi air yang cukup juga mampu membantu pengaturan berat badan, terutama bila digunakan sebagai pengganti minuman manis yang tinggi kalori.</p>

    <h4>Kesimpulan</h4>
    <p>Air putih adalah salah satu elemen paling sederhana namun sangat penting untuk menjaga kesehatan. Biasakan minum minimal 8 gelas air putih per hari, atau lebih sesuai kebutuhan tubuh dan aktivitas fisik. Dengan mencukupi asupan cairan, tubuh akan terasa lebih segar, berenergi, dan sistem organ dapat berfungsi secara optimal.</p>

