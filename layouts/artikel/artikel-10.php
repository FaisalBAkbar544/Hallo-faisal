    <h1>Menjaga Kesehatan Jantung Sejak Dini</h1>

    <h2>Faktor Risiko Penyakit Jantung</h2>
    <p>Penyakit jantung dapat disebabkan oleh pola makan buruk, kurang olahraga, merokok, dan stres berlebihan. Faktor-faktor ini saling berkontribusi meningkatkan risiko gangguan jantung yang serius, seperti serangan jantung dan gagal jantung.</p>

    <h2>Cara Menjaga Kesehatan Jantung</h2>
    <p>Untuk menjaga kesehatan jantung, penting untuk memperbanyak konsumsi sayur dan buah yang kaya antioksidan dan serat. Rutin berolahraga minimal 30 menit sehari dapat memperkuat otot jantung dan meningkatkan sirkulasi darah. Hindari merokok karena zat berbahaya dalam rokok merusak pembuluh darah dan meningkatkan risiko penyakit kardiovaskular. Selain itu, kelola stres dengan baik melalui meditasi, relaksasi, dan aktivitas positif agar tekanan darah tetap terkontrol.</p>

    <h4>Kesimpulan</h4>
    <p>Mulailah gaya hidup sehat sejak muda agar jantung tetap kuat dan berfungsi optimal sepanjang hidup. Langkah kecil seperti pola makan seimbang dan aktivitas fisik rutin dapat memberikan manfaat besar bagi kesehatan jantung Anda.</p>

