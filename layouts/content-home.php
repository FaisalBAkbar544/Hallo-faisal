    <div class="mb-4 pl-3 border-left" style="border-left-width: 5px; border-left-color: #1e3a8a;">
        <h2><strong>Apa Itu Tes DISC?</strong></h2>
        <p> Tes DISC (Dominance, Influence, Steadiness, Conscientiousness) adalah sebuah alat untuk memahami tipe-tipe perilaku dan gaya kepribadian, 
            yang pertama kali dikembangkan oleh William Moulton Marston. Dalam penerapannya di dunia bisnis dan usaha, alat ini telah membuka wawasan dan pemikiran, 
            baik secara professional maupun secara personal.</p>
        <p>Tes DISC merupakan sebuah instrumen asesmen yang dirancang untuk mengidentifikasi dan menggambarkan gaya perilaku seseorang berdasarkan empat dimensi utama:</p>
        <ol>
            <li><strong>Dominance (D)</strong> – Fokus pada tugas, kepemimpinan, dan pengambilan keputusan cepat.</li>
            <li><strong>Influence (I)</strong> – Berorientasi pada hubungan sosial dan komunikasi persuasif.</li>
            <li><strong>Steadiness (S)</strong> – Mewakili kestabilan, kesabaran, dan kerjasama jangka panjang.</li>
            <li><strong>Conscientiousness (C)</strong> – Menekankan akurasi, ketelitian, dan kepatuhan terhadap aturan.</li>
        </ol>

        <h3 class="mt-4">Tujuan dan Fungsi Tes DISC</h3>
        <h5>Dalam Konteks Individu</h5>
        <ul>
            <li>Mengenali karakter dan cara kerja diri sendiri.</li>
            <li>Menemukan cara komunikasi paling efektif.</li>
        </ul>
        <h5>Dalam Konteks Organisasi</h5>
        <ul>
            <li>Membantu proses rekrutmen dan penempatan kerja.</li>
            <li>Meningkatkan kolaborasi dan efektivitas tim.</li>
        </ul>

        <h3 class="mt-4">Manfaat Tes DISC</h3>
        <ul>
            <li><strong>Pengembangan Diri:</strong> menyadari kekuatan pribadi dan gaya adaptif.</li>
            <li><strong>Hubungan Interpersonal:</strong> menghindari miskomunikasi dan membangun empati.</li>
            <li><strong>Kinerja Tim:</strong> membentuk tim yang seimbang.</li>
            <li><strong>Penjualan & Pelayanan:</strong> menyesuaikan pendekatan dengan pelanggan.</li>
            <li><strong>Kepemimpinan:</strong> mengenali dan menyesuaikan gaya kepemimpinan.</li>
        </ul>

        <h3 class="mt-4">Sejarah Tes DISC</h3>
        <p>Teori DISC dikembangkan oleh <strong>Dr. William Moulton Marston</strong> tahun 1928, dan alat asesmennya dikembangkan oleh <strong>Walter V. Clarke</strong> pada 1956. Berbagai versi modern sekarang digunakan oleh organisasi seperti Wiley (Everything DiSC®), Thomas International, dan lainnya.</p>

        <h3 class="mt-4">Jenis Hasil Tes DISC</h3>
        <ul>
            <li>Tipe utama dan sekunder perilaku.</li>
            <li>Grafik perilaku di situasi berbeda.</li>
            <li>Deskripsi tentang motivasi, ketakutan, dan gaya kerja.</li>
        </ul>

        <h3 class="mt-4">Apakah Tipe DISC Bisa Berubah?</h3>
        <p>Ya, DISC mencerminkan <em>perilaku saat ini</em> yang bisa berubah tergantung lingkungan, pengalaman, peran, atau tekanan.</p>

        <h3 class="mt-4">Kesimpulan</h3>
        <p>Tes DISC membantu kita memahami perilaku diri sendiri dan orang lain, meningkatkan komunikasi, hubungan, efektivitas kerja, dan kualitas hidup secara keseluruhan.</p>
